        <h1 class="text-2xl font-bold mb-4">Sera ya Faragha</h1>
        <p class="mb-4">TARIQ inaheshimu faragha yako na kusambaza taarifa ya mtu binafsi tu kama inahitajika kutoa huduma. Sera hii inaeleza tunachokusanya, kwa nini tunakusanya, jinsi tunavyotumia, na haki zako.</p>

        <h2 class="text-lg font-semibold mt-4">1. Data Tunachoikusanya</h2>
        <ul class="list-disc pl-6 mb-3">
            <li>Taarifa ya kitambulisho na wasiliana (jina, barua pepe, simu, kitambulisho cha kitaifa).</li>
            <li>Taarifa ya elimu na kazi (chuo kikuu, kozi, shahada, GPA, historia ya ajira).</li>
            <li>Maudhimisho ya wasifu na sanaa (ujuzi, sanaa za kuzama, upeo).</li>
            <li>Taarifa ya matumizi (kuingia, ukurasa uliozuru, matumizi ya sifa) na taarifa ya vifaa.</li>
            <li>Taarifa nyingine yoyote unayotoa kwa hiari.</li>
        </ul>

        <h2 class="text-lg font-semibold mt-4">2. Basi za Kisheria kwa Kusambaza</h2>
        <p class="mb-3">Tunasambaza taarifa ya mtu binafsi ambapo inahitajika kutekeleza mgogoro na wewe (kusisimamia akaunti, kulingana), kuridhimika na wajibu wa kisheria, kulinza maslahi yasiyofaa, au ambapo tuna riba ya kisheria (uboreshaji wa huduma, kiazazi cha udanganyifu) kudimbilia dhidi ya haki zako za faragha. Ambapo inahitajika, tutapata maafikiano kwa shughuli fulani za kusambaza.</p>

        <h2 class="text-lg font-semibold mt-4">3. Lengo la Kusambaza</h2>
        <p class="mb-3">Tunatumia data kwa: kutoa Jukwaa, kuthibitisha vitambulisho, kulingana wazamili wa baadhi na ajira, kuundwa uchambuzi usio na kitambulisho kwa washirikiano, kugundua na kukomeza udanganyifu, na kuridhimika na wajibu wa kisheria.</p>

        <h2 class="text-lg font-semibold mt-4">4. Kusambaza Data na Kufichua</h2>
        <p class="mb-3">Hatusambazi taarifa ya mtu binafsi. Tunaweza kufichua taarifa ya mtu binafsi kwa: ajira zilizoidhinishwa (na ruhusa yako), watoaji wa huduma na waendeshaji chini ya mgogoro, miili ya serikali ikiwa ilihitaji, na wanunuzi kwa tukio la kumuza biashara. Wanapokea wote wanahitajika kusambaza data kwa mujibu wa sera hii na sheria inatumika.</p>

        <h2 class="text-lg font-semibold mt-4">5. Kutengeneza Data</h2>
        <p class="mb-3">Tunatunza taarifa ya mtu binafsi tu kwa kile kinachohitajika kutoa huduma na kukutana na wajibu wa kisheria. Mipango ya kutengeneza inatofautiana kwa aina ya data; ambapo inakamatia tunapunguza kutengeneza na kusisimamia data kutumika kwa uchambuzi.</p>

        <h2 class="text-lg font-semibold mt-4">6. Usalama</h2>
        <p class="mb-3">Tunatunza hatua za busara za mtaalamu, ziada, na kimwili kutengeneza taarifa ya mtu binafsi, ikiwa ni pamoja na usimbaji ambapo unakupya na kusambaza, kudhibiti ufikiaji, na kuandika. Hii hatari, hakuna usalama kamili; simu udanganyifu kwa haraka.</p>

        <h2 class="text-lg font-semibold mt-4">7. Uhamaji wa Kimataifa</h2>
        <p class="mb-3">TARIQ inaweza kuhamia na kutengeneza taarifa ya mtu binafsi nje ya nchi yako. Ambapo uhamaji unafanyika, tunatumia kinga zinazofaa kama mgogoro na kauli za kawaida za kulinda data au tutegemei mfumo wa kisheria wa nchi inayoruhusu uhamaji.</p>

        <h2 class="text-lg font-semibold mt-4">8. Haki Zako</h2>
        <p class="mb-3">Chini ya sheria ya ndani, unaweza kuomba ufikiaji wa taarifa yako ya mtu binafsi, kusahihisha, kufuta, kuzuia kusambaza, kuflani, au kupinga kusambaza. Kusambaza haki wasiliana na msaada (tazama chini). Tunaweza kuomba uthibitisho wa kitambulisho na kunaweza kukataa maombi yasiyofaa au yasiyo na mipango.</p>

        <h2 class="text-lg font-semibold mt-4">9. Watoto</h2>
        <p class="mb-3">Jukwaa sio kwa watoto chini ya 18. Hatukusambazi kwa busara taarifa ya mtu binafsi kutoka kwa watoto; ikiwa kugundulika tutafuta na kusimama akaunti.</p>

        <h2 class="text-lg font-semibold mt-4">10. Vidakuzi & Ufuataji</h2>
        <p class="mb-3">Tunatumia vidakuzi na teknolojia sawa kwa uthibitisho, upendeleo, uchambuzi, na usalama. Unaweza kudhibiti mpangilio wa vidakuzi katika kivinjari chako; kulemaza vidakuzi kunaweza kuathiri utendaji.</p>

        <h2 class="text-lg font-semibold mt-4">11. Huduma za Watu wa Tatu</h2>
        <p class="mb-3">Tunaungana na huduma za watu wa tatu (mfano, barua pepe, uchambuzi, ramani). Watoaji hao wana sera zao za faragha; tunapendekeza kuangalia sera zao.</p>

        <h2 class="text-lg font-semibold mt-4">12. Onyo la Ubomozi</h2>
        <p class="mb-3">Ikiwa ubomozi wa taarifa ya mtu binafsi unafanyika ambao unaweza kusababisha hatari kubwa kwa haki na uhuru, tutafahamisha watumiaji wanaoathiriwa na waongezaji kama inahitajika na sheria.</p>

        <h2 class="text-lg font-semibold mt-4">13. Mabadiliko ya Sera</h2>
        <p class="mb-3">Tunaweza kusasisha sera hii. Mabadiliko makubwa yatafahamishwa kwa njia ya Jukwaa au barua pepe. Matumizi yanayoendelea baada ya mabadiliko yanamaanisha kukubaliana.</p>

