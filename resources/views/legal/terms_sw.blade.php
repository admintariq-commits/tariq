        <h1 class="text-2xl font-bold mb-4">Sheria na Masharti</h1>
        <p class="mb-4">Sheria na Masharti haya ("Sheria") yanachagamia ufikiaji wako na matumizi ya jukwaa la TARIQ na huduma zake. Kwa kuunda akaunti, kusajili, au kutumia jukwaa, unakubali na kukubaliana kuwa bound na Sheria hizi. Ikiwa harakubali, usitumie huduma.</p>

        <h2 class="text-lg font-semibold mt-4">1. Ufafanuzi wa Maneno</h2>
        <p class="mb-3">"Jukwaa", "Huduma" au "TARIQ" inamaanisha wasifu wa simu, miunganisho ya masimu, API, na huduma zinazohusiana zilizopatikana na TARIQ. "Mtumiaji", "wewe" au "yako" inamaanisha mtu au shirikishi lolote linalo tumia Jukwaa.</p>

        <h2 class="text-lg font-semibold mt-4">2. Ujumbe na Hadhi ya kisheria</h2>
        <p class="mb-3">Unathibitisha na kuhakikisha kuwa uko umri wa miaka 18 au zaidi na una uwezo wa kisheria kuingia makubaliano haya. Mashirika lazima kujenga akaunti tu ikiwa wanakuwezesha kufanya hivyo.</p>

        <h2 class="text-lg font-semibold mt-4">3. Majukumu ya Akaunti</h2>
        <p class="mb-3">Lazima utoe taarifa kamili na sahihi wakati wa kusajili na kududu nenosiri lako salama. Unawajibika kabisa kwa shughuli zote chini ya akaunti yako, ikiwa ni pamoja na ufikiaji kwa watu wa tatu wanao tumia nenosiri lako. Lazima kumfahamisha TARIQ mara moja kuhusu ufikiaji usio ruhusiwa.</p>

        <h2 class="text-lg font-semibold mt-4">4. Matumizi Yanayokubaliana</h2>
        <p class="mb-3">Unakubali usitumie Jukwaa kwa: (a) kupakua au kusambaza maudhimisho yasiyoruhusiwa, yasiyo na hatari, au kubomoa; (b) kuigiza wengine; (c) harakati za kuingilia, kulemaza, au kuzuia usalama wa Jukwaa; au (d) kukusanya au kutumia taarifa ya mtu binafsi wa watumiaji wengine bila ruhusa. Kuzuia kunaweza kusababisha kusimama au kukatishwa kabisa.</p>

        <h2 class="text-lg font-semibold mt-4">5. Uthibitisho wa Kitambulisho</h2>
        <p class="mb-3">TARIQ inaweza kuomba uthibitisho wa kitambulisho au nyaraka kwa sifa fulani. Unakubali uthibitisho huo na kuzika kuwa kutoa hati au taarifa za uongo ni sababu ya kusimamishwa mara moja na kuripotiwa kwa mamlaka.</p>

        <h2 class="text-lg font-semibold mt-4">6. Usahihi wa Data na Maafikiano ya Mtumiaji</h2>
        <p class="mb-3">Unathibitisha kuwa taarifa unayotuma ni sahihi, kamili na ya sasa. Unakubaliana kusahihisha haraka udhaifu wowote. TARIQ haisomezi taarifa zote zilizotumwa na watumiaji na haichukui dhamana ya upangaji kwa taarifa za watumiaji.</p>

        <h2 class="text-lg font-semibold mt-4">7. Haki za Akili</h2>
        <p class="mb-3">Maudhimisho yote ya jukwaa, programu, na muundo wana miliki ya TARIQ au wamekodishwa kwa TARIQ. Huwe unaweza kunukuliana, kusambaza, kurekebisha, au kuundwa kazi zinazotolewa bila ruhusa ya maandishi. Unatoa kwa TARIQ leseni isiyoeksklusibiti, inayoweza kusambazwa, na duniani kuhusika na maudhimisho unayopakua kwa uendeshaji na uboreshaji wa huduma (ikiwa ni pamoja na uchambuzi sisiyo na kitambulisho).</p>

        <h2 class="text-lg font-semibold mt-4">8. Ada na Malipo</h2>
        <p class="mb-3">Sifa fulani zinaweza kulingana na ada. Ambapo ada inatumika, masharti ya malipo yatasalimu kabla ya ununuzi. Ada zote sio-refundable isipokuwa kama inahitajika na sheria au kusema kwa maandishi.</p>

        <h2 class="text-lg font-semibold mt-4">9. Usalama na Ukufuateni</h2>
        <p class="mb-3">TARIQ inayafanya mbinu za kufua na kutegemea kwa juu ya usalama ili kulinda data. Lakini, hakuna mfumo usio na vipumuaji — TARIQ haipatani hakika kwa usalama kamili. Lazima pia kuchukua hatua za busara kutengeneza nenosiri lako na vifaa.</p>

        <h2 class="text-lg font-semibold mt-4">10. Kusimama na Kusambaza</h2>
        <p class="mb-3">TARIQ inaweza kusimama au kusambaza akaunti kwa kuzuia, udanganyifu, au shughuli yasiyoruhusiwa bila onyo. Unaweza kusambaza akaunti yako wakati wowote kwa kuwasiliana na msaada. Kusambaza hakutoshezi wewe utajibika haddu zilizofika kabla ya kusambaza.</p>

